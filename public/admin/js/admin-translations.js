function getAdminLanguage() {
    return localStorage.getItem('adminLang') || 'ja';
}

/**
 * HÃ m Ä‘áº·t ngÃ´n ngá»¯
 */
function setAdminLanguage(lang) {
    localStorage.setItem('adminLang', lang);
    applyAdminTranslations();

    // Dispatch custom event Ä‘á»ƒ cÃ¡c trang cÃ³ thá»ƒ cáº­p nháº­t láº¡i dá»¯ liá»‡u
    window.dispatchEvent(new CustomEvent('adminLanguageChanged', { detail: { lang } }));
}

/**
 * HÃ m dá»‹ch theo key
 */
function t(key) {
    const lang = getAdminLanguage();
    return adminTranslations[lang]?.[key] || adminTranslations['en']?.[key] || key;
}

/**
 * Ãp dá»¥ng translations cho trang
 */
function applyAdminTranslations() {
    const lang = getAdminLanguage();
    const trans = adminTranslations[lang] || adminTranslations['en'];

    // Cáº­p nháº­t title
    const titleEl = document.querySelector('[data-i18n="page_title"]');
    if (titleEl) {
        document.title = trans[titleEl.getAttribute('data-i18n')] || document.title;
    }

    // Cáº­p nháº­t táº¥t cáº£ elements cÃ³ data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (trans[key]) {
            // Xá»­ lÃ½ cho cÃ¡c trÆ°á»ng há»£p khÃ¡c nhau
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                if (el.hasAttribute('placeholder')) {
                    el.placeholder = trans[key];
                }
            } else {
                el.innerHTML = trans[key];
            }
        }
    });

    // Cáº­p nháº­t placeholders riÃªng
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (trans[key]) {
            el.placeholder = trans[key];
        }
    });

    // Cáº­p nháº­t html lang attribute
    document.documentElement.lang = lang;

    // Cáº­p nháº­t active state cho language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
}

